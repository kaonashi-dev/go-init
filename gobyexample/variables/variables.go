package main

import (
	"fmt"
)

func main() {

	// Variables
	var a = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "apple"
	fmt.Println(f)

	// Constants

	const s string = "constant"

	fmt.Println(s)
	const n = 500000000
	fmt.Println(n)

}
